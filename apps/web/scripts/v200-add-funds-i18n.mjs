TRANSLATIONS.fr_ci = TRANSLATIONS.fr;
TRANSLATIONS.fr_cm = TRANSLATIONS.fr;

const LOCALE_ORDER = [
  "fr", "fr_ci", "fr_cm",
  "en", "de", "es", "it", "pt",
  "ar", "zh", "ja", "ko", "hi", "ru",
  "sw", "yo", "ha", "ig", "am", "om", "ff", "pcm", "wo", "zu", "ln", "ak", "lb",
];

function patchLocale(locale) {
  const file = join(LOCALES_DIR, `${locale}.ts`);
  let content;
  try {
    content = readFileSync(file, "utf8");
  } catch {
    console.log(`  ❌ ${locale}.ts introuvable — skip`);
    return;
  }
  if (content.includes('"funds.tab":')) {
    console.log(`  ⚠️  ${locale}.ts : clés V200 déjà présentes — skip`);
    return;
  }
  const dict = TRANSLATIONS[locale];
  if (!dict) {
    console.log(`  ❌ ${locale} : aucune traduction définie — skip`);
    return;
  }
  // Construire les lignes à insérer (4 espaces d'indentation pour matcher le style existant)
  const lines = KEYS_ORDER.map((key) => {
    const value = dict[key].replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    return `    "${key}": "${value}",`;
  });
  // Insérer juste avant la dernière ligne `};`
  const patched = content.replace(/^};$/m, `${lines.join("\n")}\n};`);
  if (patched === content) {
    console.log(`  ❌ ${locale}.ts : motif "};" introuvable — skip`);
    return;
  }
  writeFileSync(file, patched, "utf8");
  console.log(`  ✅ ${locale}.ts (+${KEYS_ORDER.length} clés)`);
}

console.log("=== V200 — Ajout 20 clés × 27 locales ===");
for (const locale of LOCALE_ORDER) patchLocale(locale);
console.log("\n=== Terminé ===");
