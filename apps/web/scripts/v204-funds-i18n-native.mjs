// ---- Patcher .ts (remplace les valeurs existantes ; idempotent) ----

function patchLocaleNative(code, dict) {
  const file = join(LOCALES_DIR, `${code}.ts`);
  let content;
  try {
    content = readFileSync(file, "utf8");
  } catch {
    console.log(`  ✗ ${code}.ts introuvable — skip`);
    return { replaced: 0 };
  }
  let replaced = 0;
  for (const [key, value] of Object.entries(dict)) {
    const escaped = value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    // Remplace toute valeur existante de cette clé par la version native
    const re = new RegExp(
      `("${key.replace(/\./g, "\\.")}":\\s*)"[^"]*"`,
      "g",
    );
    const before = content;
    content = content.replace(re, `$1"${escaped}"`);
    if (content !== before) replaced++;
  }
  if (replaced > 0) {
    writeFileSync(file, content, "utf8");
    console.log(`  ✓ ${code}.ts (${replaced} clés natives)`);
  } else {
    console.log(`  · ${code}.ts (aucune clé remplacée — clés introuvables ?)`);
  }
  return { replaced };
}

